CREATE VIEW [dbo].[altAllPlayers] as 
Select 
a.SEASON,
a.PLAYER_ID,
a.FIRSTNAME, 
a.LASTNAME,
a.AGE, 
a.GP, 
a.MINS, 
a.PLUS_MINUS, 
a.AST, 
a.BLK, 
a.BLKA, 
a.OREB, 
a.DREB, 
a.FG_PCT, 
a.FG3_PCT, 
a.FG3A, 
a.FG3M, 
a.FGA, 
a.FGM, 
a.FT_PCT, 
a.FTA, 
a.FTM, 
a.W, 
a.L, 
a.W_PCT, 
a.PF, 
a.PFD, 
a.REB, 
a.TOV, 
a.STL, 
a.PTS
from
(
Select
SEASON,
PLAYER_ID,
FIRSTNAME, 
Lastname, 
MAX(Player.SEASON) OVER (PARTITION BY player_id) as max_year,
AGE, 
GP, 
MINS, 
PLUS_MINUS, 
AST, 
BLK, 
BLKA, 
OREB, 
DREB, 
FG_PCT, 
FG3_PCT, 
FG3A, 
FG3M, 
FGA, 
FGM, 
FT_PCT, 
FTA, 
FTM, 
W, 
L, 
W_PCT, 
PF, 
PFD, 
REB, 
TOV, 
STL, 
PTS
from Player 
) a
where a.season = a.max_year 


